
package java.ci.pipeline;

class JavaCiPipeline {
    
    public JavaCiPipeline(){
    }

    public static double add(double x, double y){
        return x+y;
    }
    public static double rest(double x, double y){
        return x-y;
    }
    public static double divide(double x, double y){
        return x/y;
    }
    public static double multiplie(double x, double y){
        return x*y;
    }
    
}
